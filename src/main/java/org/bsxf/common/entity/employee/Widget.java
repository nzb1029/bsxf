public class Widget extends IdentityII {
	@Description(description = "部件名称", req = "true")
	private String name;
	@Description(description = "部件地址", req = "true")
	private String url;
	/**
	 * @变量 status : 状态
	 */
	private String status = "enabled";
	/**
	 * @变量 col : 列
	 */
	private String col;
	/**
	 * @变量 row : 行
	 */
	private String row;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getCol() {
		return col;
	}

	public void setCol(String col) {
		this.col = col;
	}

	public String getRow() {
		return row;
	}

	public void setRow(String row) {
		this.row = row;
	}
}
